class ternary{
    public static void main(String[] args) {
        int num1 = 2;
        int num2 = 5;

        String result = num1==num2 ? "Equal" : "Not Equal";
        System.out.println("The numbers are: " + result);

        int number1 = 2;
        int number2 = 2;

        String result2 = (number1==number2) ? "Equal" : "Not Equal";
        System.out.println("The numbers are: " + result2);
    }
}
