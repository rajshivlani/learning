class lop {
    
    public static void main(String a[]){
        

        // while loop
        int n = 0;
        
        while (n <= 3){
            System.out.println(n);
            n++;
        }

        System.out.println("while loop over then n value is : " + n);



        // do while loop

        int n2 = 5;
        do {
            System.out.println(n2);
            n2++;
        } while (n2 <= 3);



        
        //for loop
        for (int i = 0; i<=3; i++) {
            System.out.println(i);
        }
    }

}